function toTraditional(value: string) {
  return traditionalReplacements.reduce((text, [from, to]) => text.split(from).join(to), value);
}

function interpolate(value: string, params?: Params) {
  if (!params) return value;
  return Object.entries(params).reduce(
    (text, [key, replacement]) => text.replace(new RegExp(`\\{${key}\\}`, 'g'), String(replacement)),
    value,
  );
}

export function translate(key: string, locale: AppLocale, params?: Params) {
  const source = locale === 'en-US' ? (enUS[key] || key) : locale === 'zh-TW' ? toTraditional(key) : key;
  return interpolate(source, params);
}

export function normalizeLocale(language?: string): AppLocale {
  const value = (language || '').replace('_', '-').toLowerCase();
  if (value.includes('hant') || value === 'zh-tw' || value === 'zh-hk') return 'zh-TW';
  if (value.startsWith('zh')) return 'zh-CN';
  return 'en-US';
}

